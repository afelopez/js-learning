console.log("Tipos de variables en JavaScript:");

// String
let cadena = "Hola, mundo!";
console.log("String:", cadena);

// Number
let numero = 42;
console.log("Number:", numero);

// Boolean
let booleano = true;
console.log("Boolean:", booleano);

// Undefined
let indefinido;
console.log("Undefined:", indefinido);

// Null
let nulo = null;
console.log("Null:", nulo);

// Object
let objeto = { nombre: "Juan", edad: 30 };
console.log("Object:", objeto);

// Array
let arreglo = [1, 2, 3, 4, 5];
console.log("Array:", arreglo);

// Function
let funcion = function() {
    return "Soy una funci√≥n";
};
console.log("Function:", funcion);

// Symbol
let simbolo = Symbol("simbolo");
console.log("Symbol:", simbolo);

// BigInt
let bigInt = BigInt(9007199254740991);
console.log("BigInt:", bigInt);
// NaN
let noEsUnNumero = NaN;
console.log("NaN:", noEsUnNumero);